<template>
  <div class="">
    <Swiper>
      <SwiperItem v-for="item in banners">
        <a :href="item.Link">
          <img :src="item.image" alt="" @load="imageLoad"/>
        </a>
      </SwiperItem>
    </Swiper>
  </div>
</template>

<script>
import {Swiper,SwiperItem} from 'components/common/swiper'

export default {
  name: "HomeSwiper",
  components:{
    Swiper,
    SwiperItem
  },
  methods:{
    imageLoad(){
      if(!this.isLoad){
        this.$emit('swiperImageLoad')
        this.isLoad = true
      }
    }
  },
  data() {
    return {
      isLoad: false,
    };
  },
  props:{
    banners:{
      type:Array,
      default(){
        return []
      }
    }
  }
};
</script>
